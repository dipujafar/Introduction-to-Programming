#include <stdio.h>
int main()
{
    float a = 6;
    int b = 4;
    int sum = a + b;
    printf("summation = %d\n", sum);

    int sub = a - b;
    printf("substraction = %d\n", sub);

    int mul = a * b;
    printf("multiplication = %d\n", mul);

    float div = a / b;
    printf("division = %.2f\n", div);



    return 0;
}